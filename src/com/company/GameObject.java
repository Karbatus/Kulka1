package com.company;
/**
 * Abstrakcyjna klasa opisująca parametry obiektów w grze
 */
public abstract class GameObject {
    /**
     * Współrzędna x obiektu
     */
    public int x;
    /**
     * Współrzędna y obiektu
     */
    public int y;
    /**
     * Szerokość obiektu
     */
    public int width;
    /**
     * wysokość obiektu
     */
    public int height;

}
